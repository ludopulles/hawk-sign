/* see inner.h */
int Zf(verify_NTT)(const uint8_t *restrict h,
	const int16_t *restrict s1,
	const int16_t *restrict q00, const int16_t *restrict q01,
	unsigned logn, uint8_t *restrict tmp)
{
	/*
	 * Recover, using fixed-point arithmetic s0 by:
	 *     s0 = round(h0 / 2 + (h1 / 2 - s1) q01 / q00).
	 */
	size_t n, hn, u, v, w, sh_s1, sh_q00, sh_q01, sh_back;
	uint8_t hash, *atmp;
	int16_t bound_s0, *s0;
	int32_t *t0, *t1, *t2;
	int64_t cst_term;

	n = MKN(logn);
	hn = n >> 1;
	sh_s1 = 29 - (1 + Zf(bits_s1)[logn]);
	sh_q00 = 29 - Zf(bits_q00)[logn];
	sh_q01 = 29 - Zf(bits_q01)[logn];
	sh_back = sh_s1 + sh_q01 - sh_q00 - (logn - 1);

	t0 = (int32_t *)tmp;
	t1 = t0 + n;
	t2 = t1 + n;

	s0 = (int16_t *)tmp;
	atmp = (uint8_t *)(s0 + n);

	/*
	 * Throughout FFT, the following quantities are bounded in size as follows:
	 * - (h1 - 2 s1): 2^(1 + Zf(bits_s1)[logn])
	 * - q01:         2^Zf(bits_q01)[logn],
	 * - q00:         2^Zf(bits_q00)[logn],
	 * where the maximum is determined by running keygen and sign a great
	 * number of times and then rounding up 6 standard deviations to the
	 * smallest power of two.
	 *
	 * Therefore left shifting by 29 - x (where x is the exponent) eliminates
	 * overflows.
	 *
	 * Convert (h1 - 2 s1), q00 and q01 to fixed point FFT representation.
	 */
	if (logn <= 3) {
		for (w = 0; w < n; w ++) {
			t0[w] = (int32_t)(((h[1] >> w) & 1) - 2 * s1[w]) * (1 << sh_s1);
		}
	} else {
		for (u = n >> 3, w = 0; w < n; u ++) {
			hash = h[u];
			for (v = 0; v < 8; v ++, w ++) {
				t0[w] = (int32_t)((hash & 1) - 2 * s1[w]) * (1 << sh_s1);
				hash >>= 1;
			}
		}
	}

	for (u = 0; u < n; u++) {
		t1[u] = (int32_t)q01[u] * (1 << sh_q01);
		t2[u] = (int32_t)q00[u] * (1 << sh_q00);
	}

	/*
	 * Handle the constant coefficient of q00 separately as this one follows a
	 * chi-squared distribution.
	 */
	t2[0] = 0;

	fpp_FFT(t0, logn);
	fpp_FFT(t1, logn);
	fpp_FFT(t2, logn);

	cst_term = (int64_t)q00[0] * (1 << (sh_q00 - (logn - 1)));
	for (u = 0; u < hn; u++) {
		int64_t re, im, div;

		div = cst_term + (int64_t)t2[u];
		re = (int64_t)t0[u] * t1[u     ] - (int64_t)t0[u + hn] * t1[u + hn];
		im = (int64_t)t0[u] * t1[u + hn] + (int64_t)t0[u + hn] * t1[u     ];

		t0[u     ] = re / div;
		t0[u + hn] = im / div;
	}

	fpp_iFFT(t0, logn);

	/*
	 * Recover s0 with s0 = round(h0 / 2 + (h1 / 2 - s1) q01 / q00).
	 */
	if (logn <= 3) {
		for (w = 0; w < n; w ++) {
			t0[w] = fpp_of((h[0] >> w) & 1, sh_back) + t0[w];
			s0[w] = fpp_rint(t0[w], sh_back + 1);
		}
	} else {
		for (u = 0, w = 0; w < n; u ++) {
			hash = h[u];
			for (v = 0; v < 8; v ++, w ++) {
				t0[w] = fpp_of(hash & 1, sh_back) + t0[w];
				s0[w] = fpp_rint(t0[w], sh_back + 1);
				hash >>= 1;
			}
		}
	}

	/*
	 * If there is a coefficient in s0 with too large absolute value, reject
	 * the signature. The bound is chosen such that is extremely unlikely to
	 * reject a valid signature because of this.
	 * However, this bound is need to do a norm check with two primes of 31
	 * bits which does not accept invalid signatures with a squared norm that
	 * wraps around both primes in a way that it becomes small modulo both
	 * primes.
	 */
	bound_s0 = (int16_t)(1U << Zf(bits_s0)[logn]) - 1;
	for (u = 0; u < n; u++) {
		if (s0[u] < -bound_s0 || s0[u] >= bound_s0) {
			/*
			 * This does not break constant-time principle, as there are no
			 * hidden variables used in verification.
			 */
			return 0;
		}
	}

	return uncompressed_verify_NTT(h, s0, s1, q00, q01, logn, atmp);
}
